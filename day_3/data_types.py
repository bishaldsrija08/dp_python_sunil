
# Data types in Python => Data types are used to store values in variables.
# Common data types include:
# 1. Integer (int): Whole numbers, positive or negative, without decimals.
age = 25  # Example of an integer

# 2. Float (float): Numbers with decimal points.
height = 5.9  # Example of a float

# 3. String (str): Sequence of characters, enclosed in single or double quotes.
name = "Alice"  # Example of a string

# 4. Boolean (bool): Represents one of two values: True or False.
is_student = True  # Example of a boolean

# 5. List (list): Ordered collection of items, which can be of different data types.
fruits = ["apple", "banana", "cherry"]  # Example of a list

# 6. Tuple (tuple): Ordered collection of items, similar to lists, but immutable (cannot be changed).
coordinates = (10.0, 20.0)  # Example of a tuple

# 7. Dictionary (dict): Collection of key-value pairs, where each key is unique.
person = {"name": "Bob", "age": 30}  # Example of a dictionary

# 8. Set (set): Unordered collection of unique items.
unique_numbers = {1, 2, 3, 4, 5}  # Example of a set