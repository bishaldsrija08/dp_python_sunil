# Arithematic Operators Examples (+ , - , * , / , % , // , **)
a = 10
b = 3
print("Addition:", a + b)  # 13
print("Subtraction:", a - b)  # 7
print("Multiplication:", a * b)  # 30
print("Division:", a / b)  # 3.3333...
print("Modulus:", a % b)  # 1
print("Floor Division:", a // b)  # 3
print("Exponentiation:", a**b)  # 1000

# Comparison Operators Examples (== , != , > , < , >= , <=)
print("Equal:", a == b)  # False
print("Not Equal:", a != b)  # True
print("Greater Than:", a > b)  # True
print("Less Than:", a < b)  # False
print("Greater Than or Equal To:", a >= b)  # True
print("Less Than or Equal To:", a <= b)  # False

# Logical Operators Examples (and , or , not)
x = True
y = False

print("Logical AND:", x and y)  # False
print("Logical OR:", x or y)  # True
print("Logical NOT x:", not x)  # False

# Assignment Operators Examples (= , += , -= , *= , /=)
c = 5
print("Initial c:", c)  # 5
c += 2  # c= c + 2
print("After c += 2:", c)  # 7

c -= 3  # c= c - 3
print("After c -= 3:", c)  # 4

c *= 4  # c= c * 4
print("After c *= 4:", c)  # 16

c /= 2  # c= c / 2
print("After c /= 2:", c)  # 8.0
