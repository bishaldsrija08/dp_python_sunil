# WAP to find the area of rectangel using a funciton.

def area_of_rectangle(length, breadth):
    area = length * breadth
    return area

area = area_of_rectangle(5,6)
area_2 = area_of_rectangle(7,3)
area_3 = area_of_rectangle(10,4)

print("The area of the rectangle is:", area)
print("The area of the rectangle is:", area_2)
print("The area of the rectangle is:", area_3)